package ab61;

public class SwitchClass 
{ 
	
	public static void main(String[] args) 
    {
    switch(1)
    {
    case 1:   
                    System.out.println("Chrome Browser");
                                    
    case 2:        
                    System.out.println("Firefox Browser");
                    break;        
    case 3:
                    System.out.println("Safari Browser");
                    break;        
    case 4:
                    System.out.println("IE Browser");
                    break;        
                    
    }
	}
}
